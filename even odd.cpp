/*Q#2 Write a program to that takes a number from the user,
 and check whether the number is odd or even. Using Nested-if 	statements.*/
 #include<iostream>
 using namespace std;
 int main()
 {
 	int n;
 	cout<<"Enter a number"<<endl;
 	cin>>n;
 	if(n%2==0)
	 cout<<"The number "<<n<<" is even";
 	else
 	 cout<<"The number "<<n<<" is odd"; 
return 0;
 	
 }